let n=[2,1,3,7];
for(let i=0;i<n.length;i++){
    switch (n[i]){
        case 1:
            console.log("print number was one");
            break;
        case 2:
            console.log("print number was two");
            break;
        case 3:
            console.log("print number was three");
            break;
        default:
            console.log("default case");
    }
}